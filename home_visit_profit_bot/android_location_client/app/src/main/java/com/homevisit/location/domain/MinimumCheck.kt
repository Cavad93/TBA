package com.homevisit.location.domain

/**
 * Минимальный чек по адресу — ядро «личной поездки» (Ф11.5) и быстрой оценки (Ф11.1).
 *
 * В личном режиме нет ни дохода, ни вердикта «стоит ли ехать»: человек не зарабатывает,
 * а просто хочет знать, во сколько ему обойдётся съездить туда и обратно. Сервер считает
 * то же ядро `minimum_check`, что питает публичный SEO-калькулятор поездки — цифры сходятся.
 */
data class MinimumCheck(
    val address: String,
    /** Дорога туда И обратно (уже удвоено сервером). */
    val roundTripKm: Double,
    val roundTripMinutes: Double,
    /** Километры туда-обратно × себестоимость км (топливо + износ). */
    val carCost: Double,
    /** Минуты дороги × личная норма ₽/час — время в пути тоже стоит денег. */
    val timeCost: Double,
    /** Нижняя граница платной парковки у точки за визит. */
    val parkingCost: Double,
    /** Итог: во столько обойдётся поездка туда-обратно (без часа на месте). */
    val minimumCheck: Double,
    /** +N ₽ за каждый час на месте (= личная норма ₽/час), если человек там задержится. */
    val hourlyOnSite: Double,
    /** true — адрес вне покрытия карт, дорога посчитана по прямой (грубая оценка). */
    val fallback: Boolean,
    /** Билеты дешевле машины (Ф11.6) или null — сравнивать нечего, кнопки нет. */
    val tickets: TicketsOffer? = null,
)

/**
 * «Дешевле долететь» (Ф11.6) — приходит от сервера ТОЛЬКО когда выгода реальная.
 *
 * Все решения приняты на сервере: личный режим, межгород >200 км, билет дешевле машины
 * минимум на порог (дефолт 10% — почти равная цена это шум, а не информация). Клиенту
 * думать не о чем: пришёл блок — показываем кнопку, не пришёл — её нет.
 *
 * ВАЖНО: цены круговые — `priceFrom` это перелёт туда И обратно, и сравнивали его с
 * дорогой на машине туда-обратно. Рядом с режимом «только туда» этот блок показывать
 * нельзя: там цена машины делится пополам, а билет — нет, и «дешевле» станет неправдой.
 */
data class TicketsOffer(
    /** Минимальная кешированная цена билета туда-обратно, ₽. */
    val priceFrom: Double,
    /** На сколько процентов дешевле машины. */
    val savingsPercent: Int,
    /** Готовая фраза от сервера — цифры не пересобираем, чтобы не разошлись. */
    val text: String,
    /** Партнёрская ссылка на поиск с нашим маркером. */
    val url: String,
)

/**
 * Ответ быстрой оценки. `check != null` — посчитали; иначе `reason` объясняет, чего
 * не хватило (нет адреса/координат/местоположения или сеть недоступна).
 */
data class QuickEstimateResult(
    val check: MinimumCheck? = null,
    val reason: String? = null,
)
