"""Нормализация корпусов/строений/литер в адресе (Фаза 13.3).

«5к1», «5 корп 1», «стр 3», «литА» пишут по-разному, и эти хвосты портят похожесть
при геокодинге: «Ленина 5к1» и «Ленина 5 корпус 1» — один дом, но строки разные.
Здесь приводим маркер к каноничному виду («корпус 1», «строение 3», «литера А»),
чтобы DaData/pg_trgm видели одинаковое. Сам номер дома не трогаем.

Осторожно с «к» и «с»: это ещё и предлоги. Поэтому маркер принимаем только когда он
прилеплен к цифре («5к1») или явно написан («корп»/«строение»), а голую «к»/«с» с
пробелом (предлог) не трогаем.
"""

from __future__ import annotations

import re

# Прилеплено к номеру дома: «5к1», «5с3», «5к.1».
_ATTACHED = [
    (re.compile(r"(?<=\d)\s*к\.?\s*(\d+[а-яё]?)\b", re.IGNORECASE), r" корпус \1"),
    (re.compile(r"(?<=\d)\s*с\.?\s*(\d+[а-яё]?)\b", re.IGNORECASE), r" строение \1"),
]
# Явные слова в любом написании: «корп 1», «корпус 1», «стр 3», «строение 3», «лит А».
_SPELLED = [
    (re.compile(r"\b(?:корпус|корпус\.|корп|корп\.)\s*(\d+[а-яё]?)\b", re.IGNORECASE), r"корпус \1"),
    (re.compile(r"\b(?:строение|строение\.|стр|стр\.)\s*(\d+[а-яё]?)\b", re.IGNORECASE), r"строение \1"),
    (re.compile(r"\b(?:литера|литер|лит)\.?\s*([а-яёa-z0-9]+)\b", re.IGNORECASE), r"литера \1"),
]


def canonical_building(text: str) -> str:
    """Привести маркеры корпуса/строения/литеры к каноничному виду. Номер дома не трогаем."""
    if not text:
        return text
    result = text
    # Сначала прилепленные (5к1), потом словесные (корп 1) — порядок важен, чтобы «к»
    # у цифры не съелось словесным правилом раньше времени.
    for pattern, repl in _ATTACHED:
        result = pattern.sub(repl, result)
    for pattern, repl in _SPELLED:
        result = pattern.sub(repl, result)
    # Литеру — в верхний регистр («литера а» → «литера А»).
    result = re.sub(
        r"(литера)\s+([а-яёa-z])\b",
        lambda m: f"{m.group(1)} {m.group(2).upper()}",
        result,
        flags=re.IGNORECASE,
    )
    return re.sub(r"\s+", " ", result).strip()
