from __future__ import annotations

from app.repositories import DrivingSegmentRepository

# Время в пути пешком — логистика, а не физиология.
#
# Сколько занимает дорога от машины до двери, по лестнице и обратно — это операционный
# факт: он влияет на плановую длительность визита и на то, сколько заказов реально
# помещается в смену. Такие данные — обычные персональные, как «время в пути» у любого
# курьерского сервиса.
#
# А вот МАНЕРА ходьбы (темп, разброс времени шага, ровность) здесь больше не измеряется.
# У коэффициента вариации времени шага нет никакой операционной цели, кроме вывода о
# физиологическом состоянии человека, — и переименовать его в «логистический трекинг»
# не выйдет: проверяющий спросит, зачем логистике разброс времени шага, и ответить будет
# нечем. Данные, из которых «путём интеллектуальной операции сопоставления или дедукции»
# выводится чувствительная информация, сами становятся специальной категорией — это
# прямо установлено Судом ЕС (C-184/20), а Роскомнадзор трактует состояние здоровья не
# уже: к нему он относит даже сведения о трудоспособности.
#
# Точность оценки от этого падает. Зато обработка остаётся в рамках обычных данных.


def day_walk_metrics(segments: DrivingSegmentRepository, work_day_id: int) -> dict[str, float]:
    """Сколько за смену человек прошёл пешком между машиной и адресами."""
    seconds = sum(
        float(row["walk_seconds"] or 0)
        for row in segments.for_day(work_day_id)
        if int(row["walk_bouts"] or 0) > 0
    )
    if seconds <= 0:
        return {}
    # Точнее, чем по GPS: точка раз в минуту не видит проход от машины до подъезда,
    # а шаги датчик движения различает всегда.
    return {"walk_minutes": round(seconds / 60, 1)}
